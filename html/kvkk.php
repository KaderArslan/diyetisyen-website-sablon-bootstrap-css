            <li><i class="fas fa-angle-right"></i> Sayılan hususlar dışında veri işlenmesi söz konusu olduğunda şirket , sizi en hızlı şekilde bilgilendirmeye devam edebilmek için, belirtilen kategorileri sizler için ilgili metinlerde güncellemeye devam edecektir.</li></ul>
             

            <span class="fw-bold text-dark mt-3">Kişisel Verilerin Saklanması</span>
            <p class="mt-3">Elde ettiğimiz kişisel veriler, Şirket’in ticari faaliyetlerinin yerine getirebilmesi amacıyla, uygun süre zarfında fiziksel veya elektronik ortamda güvenli bir şekilde saklanmaktadır. Söz konusu faaliyetler kapsamında, Şirket tarafından kişisel verilerin korunmasına ilişkin olarak KVKK başta olmak üzere, ilgili tüm mevzuatta öngörülen yükümlülüklere uygun hareket edilmektedir. İlgili mevzuatlar uyarınca, kişisel verilerin daha uzun süre saklanmasına cevaz verilen veya zorunlu tutulan haller müstesna olmak kaydıyla, kişisel verilerin işlenme amaçlarının sona ermesi durumunda, Şirket tarafından re’sen yahut ekte bulunan veri sahibi başvuru formu aracılığıyla ve kullanılabilecek farklı teknikler ile veri sahiplerinin talebi üzerine veriler silinecek, yok edilecek yahut anonimleştirilecektir. Kişisel verilerin söz konusu yöntemler vasıtasıyla silinmesi durumunda, bu veriler tekrar hiçbir şekilde kullanılamayacak ve geri getirilemeyecek şekilde imha edilecektir. Ancak veri sorumlusunun meşru menfaatinin olduğu durumlarda, işlenme amacının ve ilgili kanunlarda belirtilen sürelerin de sona ermesine rağmen veri sahiplerinin temel hak ve özgürlüklerine zarar vermemek kaydıyla kişisel veriler, Borçlar Kanunu’nda düzenlenen genel zamanaşımı süresinin (on yıl) sona ermesine kadar saklanabilecektir. Bahsi geçen zamanaşımı süresinin sona ermesinin ardından kişisel veriler, yukarıda belirtilen prosedüre göre silinecek, yok edilecek yahut anonim hale getirilecektir.</p>

            <span class="fw-bold text-dark mt-3">Kişisel Verilerin Aktarılması</span>
            <p class="mt-3">ŞİRKET , kişisel verilerin üçüncü taraflarla paylaşılması hususunda, diğer kanunlarda yer alan hükümler saklı kalmak kaydıyla, KVKK’da düzenlenen şartlara özenle uymaktadır. Bu çerçevede, kişisel veriler, ŞİRKET tarafından veri sahibinin açık rızası olmadan üçüncü kişilere aktarılmamaktadır. Ancak, KVKK tarafından düzenlenen aşağıdaki şartlardan birinin varlığı halinde kişisel veriler ŞİRKET tarafından, veri sahibinin açık rızası temin edilmeksizin de aktarılabilecektir:</p>
            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Kanunlarda açıkça öngörülmesi,</li>
            <li><i class="fas fa-angle-right"></i> Fiili imkânsızlık nedeniyle rızasını açıklayamayacak durumda bulunan veya rızasına hukuki geçerlilik tanınmayan kişinin kendisinin ya da bir başkasının hayatı veya beden bütünlüğünün korunması için zorunlu olması,</li>
            <li><i class="fas fa-angle-right"></i> Bir sözleşmenin kurulması veya ifasıyla doğrudan doğruya ilgili olması kaydıyla, sözleşmenin taraflarına ait kişisel verilerin işlenmesinin gerekli olması, Veri sorumlusunun hukuki yükümlülüğünü yerine getirebilmesi için zorunlu olması,</li>
            <li><i class="fas fa-angle-right"></i> Veri sahibinin kendisi tarafından alenileştirilmiş olması,</li> 
            <li><i class="fas fa-angle-right"></i> Bir hakkın tesisi, kullanılması veya korunması için veri işlemenin zorunlu olması,</li> 
            <li><i class="fas fa-angle-right"></i> Veri sahibinin temel hak ve özgürlüklerine zarar vermemek kaydıyla, veri sorumlusunun meşru menfaatleri için veri işlenmesinin zorunlu olması. Yeterli önlemler alınmak kaydıyla; sağlık ve cinsel hayat dışındaki özel nitelikli kişisel veriler bakımından kanunlarda öngörülmesi, sağlık ve cinsel hayata ilişkin özel nitelikli kişisel veriler bakımından ise,</li> 
            <li><i class="fas fa-angle-right"></i> Kamu sağlığının korunması,</li>
            <li><i class="fas fa-angle-right"></i> Koruyucu hekimlik,</li>
            <li><i class="fas fa-angle-right"></i> Tıbbî teşhis,</li>
            <li><i class="fas fa-angle-right"></i> Tedavi ve bakım hizmetlerinin yürütülmesi,</li>
            <li><i class="fas fa-angle-right"></i> Sağlık hizmetleri ile finansmanının planlanması ve yönetimi gibi amaçlarla açık rıza temin edilmeksizin kişisel verileriniz aktarılabilecektir. Özel nitelikli kişisel verilerin aktarılmasında da, bu verilerin işlenme şartlarında belirtilen koşullara uyulmaktadır.</li></ul>

            <span class="fw-bold text-dark mt-3">Aydınlatma Yükümlülüğü</span>
            <p class="mt-3">KVKK’nın 10. maddesi kapsamında, veri sahiplerinin, kişisel verilerin elde edilmesinden önce yahut en geç elde edilmesi sırasında aydınlatılması gerekmektedir. Söz konusu aydınlatma yükümlülüğü çerçevesinde veri sahiplerine iletilmesi gereken bilgiler şunlardır:</p>
            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Veri sorumlusunun ve varsa temsilcisinin kimliği,</li>
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin hangi amaçla işleneceği,</li>
            <li><i class="fas fa-angle-right"></i> İşlenen kişisel verilerin kimlere ve hangi amaçla aktarılabileceği,</li>
            <li><i class="fas fa-angle-right"></i> Kişisel veri toplamanın yöntemi ve hukuki sebebi,</li>
            <li><i class="fas fa-angle-right"></i> KVKK’nın 11. maddesinde2sayılan diğer haklar.</li> </ul>


            <p class="mt-3">ŞİRKET, aydınlatma yükümlülüğünü yerine getirmek amacıyla, süreç ve verileri işlenen kişiler bazında, yukarıda belirtilen KVKK hükmü kapsamında veri sahiplerine sunulmak üzere aydınlatma beyanları hazırlamıştır. Aydınlatma beyanlarının veri sahiplerine sunulmasının ardından, ŞİRKETİN ticari faaliyetlerini yürütebilmesi için veri sahibinin açık rızasının alınmasını gerektiren veri işleme faaliyetleri ve veri kategorileri için de açık rıza beyanları hazırlanmıştır. Veri sahiplerine yönelik hazırlanan açık rıza beyanlarında, KVKK’ya dayanak teşkil eden Avrupa Birliği düzenlemelerine paralel olarak, veri sahiplerine kişisel verilerinin ŞİRKET tarafından işlenip işlenemeyeceğine dair seçim hakkı tanınmış ve açık rıza temin edilememesi halinde, meydana gelebilecek sonuçlar hakkında bilgilendirmede bulunulmuştur. Öte yandan, KVKK’nın 28(1). maddesi çerçevesinde, aşağıda sayılan durumlarda ŞİRKETİN aydınlatma yükümlülüğü bulunmamaktadır: </p>
            <p class="mt-3">Kişisel verilerin, üçüncü kişilere verilmemek ve veri güvenliğine ilişkin yükümlülüklere uyulmak kaydıyla gerçek kişiler tarafından tamamen kendisiyle veya aynı konutta yaşayan aile fertleriyle ilgili faaliyetler kapsamında işlenmesi, </p>
            <p class="mt-3">Kişisel verilerin resmi istatistik ile anonim hâle getirilmek suretiyle araştırma, planlama ve istatistik gibi amaçlarla işlenmesi, </p>
            <p class="mt-3">Kişisel verilerin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini, ekonomik güvenliği, özel hayatın gizliliğini veya kişilik haklarını ihlal etmemek ya da suç teşkil etmemek kaydıyla, sanat, tarih, edebiyat veya bilimsel amaçlarla ya da ifade özgürlüğü kapsamında işlenmesi, </p>
            <p class="mt-3">Kişisel verilerin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini veya ekonomik güvenliği sağlamaya yönelik olarak kanunla görev ve yetki verilmiş kamu kurum ve kuruluşları tarafından yürütülen önleyici, koruyucu ve istihbari faaliyetler kapsamında işlenmesi, </p>
            <p class="mt-3">Kişisel verilerin soruşturma, kovuşturma, yargılama veya infaz işlemlerine ilişkin olarak yargı makamları veya infaz mercileri tarafından işlenmesi. </p>
            <p class="mt-3">Bununla beraber, KVKK’nın 28(2). maddesi çerçevesince, ŞİRKETİN aydınlatma yükümlülüğü aşağıdaki hallerde uygulama alanı bulmayacaktır: </p>
            <p class="mt-3">Kişisel veri işlemenin suç işlenmesinin önlenmesi veya suç soruşturması için gerekli olması, </p>
            <p class="mt-3">Veri sahibinin kendisi tarafından alenileştirilmiş kişisel verilerin işlenmesi </p>
            <p class="mt-3">Kişisel veri işlemenin kanunun verdiği yetkiye dayanılarak görevli ve yetkili kamu kurum ve kuruluşları ile kamu kurumu niteliğindeki meslek kuruluşlarınca, denetleme veya düzenleme görevlerinin yürütülmesi ile disiplin soruşturma veya kovuşturması için gerekli olması, </p>
            <p class="mt-3">Kişisel veri işlemenin bütçe, vergi ve mali konulara ilişkin olarak Devletin ekonomik ve mali çıkarlarının korunması için gerekli olması. </p>

            <span class="fw-bold text-dark mt-3">Veri Sahibinin Hakları</span>
            <p class="mt-3">Mehtap Erciyes Diyetisyen, tarafından işbu Politika’da yer alan esaslara uygun olarak işlenen kişisel verilere ilişkin olarak, KVKK’nın 11. maddesinde veri sahipleri için tanınan hakların kullandırılması konusunda gerekli önlemler alınmıştır. Bahse konu haklar şunlardır: a) Kişisel veri işlenip işlenmediğini öğrenme, b) Kişisel verileri işlenmişse buna ilişkin bilgi talep etme, c) Kişisel verilerin işlenme amacını ve bunların amacına uygun kullanılıp kullanılmadığını öğrenme, d) Yurt içinde veya yurt dışında kişisel verilerin aktarıldığı üçüncü kişileri bilme, e) Kişisel verilerin eksik veya yanlış işlenmiş olması hâlinde bunların düzeltilmesini isteme, f) Kanun’un 7. maddesinde öngörülen şartlar çerçevesinde kişisel verilerin silinmesini veya yok edilmesini isteme, g) Yukarıdaki (e) ve (f) maddeleri uyarınca yapılan işlemlerin, kişisel verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme, h) İşlenen verilerin münhasıran otomatik sistemler vasıtasıyla analiz edilmesi suretiyle kişinin kendisi aleyhine bir sonucun ortaya çıkmasına itiraz etme, i) Kişisel verilerin kanuna aykırı olarak işlenmesi sebebiyle zarara uğraması hâlinde zararın giderilmesini talep etme.</p>
            <p class="mt-3">Veri sahipleri, yukarıda sayılan haklarını, işbu Politika’nın ekinde bulunan veri sahibi başvuru formunun ıslak imzalı bir nüshasını, iletişim adreslerine, posta, e- posta yahut iadeli taahhütlü mektup vasıtasıyla ileterek kullanabilirler. Formun doldurulması yahut şirkete gönderilmesi hakkında detaylı bilgiler, ekte bulunan başvuru formunda yer almaktadır. şirket , ilgili başvurulara yönelik cevabı fiziken yahut elektronik olarak ilgili veri sahibine ulaştıracaktır. Şirket , talebin niteliğine göre, talebi en kısa sürede ve en geç otuz (30) gün içinde ücretsiz olarak sonuçlandıracaktır. Ancak, işlemin ayrıca bir maliyeti gerektirmesi halinde, şirket tarafından Kurulca belirlenen tarifedeki ücret ilgililerden alınacaktır. Ayrıca, veri sahiplerinin taleplerinin sonuçlandırılması sürecinde, şirket tarafından başvuruculardan ek bilgi veyahut belge talep edilebilecektir.</p>
            <p class="mt-3">Öte yandan, KVKK’nın 28(1). maddesi çerçevesinde, veri sahipleri, aşağıda sayılan hallerde KVKK’nın 11. maddesinde sayılan yukarıdaki hakları kullanamaz:</p>
            <p class="mt-3">Kişisel verilerin, üçüncü kişilere verilmemek ve veri güvenliğine ilişkin yükümlülüklere uyulmak kaydıyla gerçek kişiler tarafından tamamen kendisiyle veya aynı konutta yaşayan aile fertleriyle ilgili faaliyetler kapsamında işlenmesi,</p>
            <p class="mt-3">Kişisel verilerin resmi istatistik ile anonim hâle getirilmek suretiyle araştırma, planlama ve istatistik gibi amaçlarla işlenmesi,</p>
            <p class="mt-3">Kişisel verilerin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini, ekonomik güvenliği, özel hayatın gizliliğini veya kişilik haklarını ihlal etmemek ya da suç teşkil etmemek kaydıyla, sanat, tarih, edebiyat veya bilimsel amaçlarla ya da ifade özgürlüğü kapsamında işlenmesi,</p>
            <p class="mt-3">Kişisel verilerin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini veya ekonomik güvenliği sağlamaya yönelik olarak kanunla görev ve yetki verilmiş kamu kurum ve kuruluşları tarafından yürütülen önleyici, koruyucu ve istihbari faaliyetler kapsamında işlenmesi,</p>
            <p class="mt-3">Kişisel verilerin soruşturma, kovuşturma, yargılama veya infaz işlemlerine ilişkin olarak yargı makamları veya infaz mercileri tarafından işlenmesi. Bununla beraber, KVKK’nın 28(2). maddesi çerçevesinde, zararın giderilmesi hakkı hariç olmak üzere, KVKK’nın 11. maddesinde sayılan yukarıdaki haklar aşağıdaki durumlarda uygulama alanı bulmayacaktır:</p>
            <p class="mt-3">Kişisel veri işlemenin suç işlenmesinin önlenmesi veya suç soruşturması için gerekli olması,</p>
            <p class="mt-3">Veri sahibinin kendisi tarafından alenileştirilmiş kişisel verilerin işlenmesi,</p>
            <p class="mt-3">Kişisel veri işlemenin kanunun verdiği yetkiye dayanılarak görevli ve yetkili kamu kurum ve kuruluşları ile kamu kurumu niteliğindeki meslek kuruluşlarınca, denetleme veya düzenleme görevlerinin yürütülmesi ile disiplin soruşturma veya kovuşturması için gerekli olması,</p>
            <p class="mt-3">Kişisel veri işlemenin bütçe, vergi ve mali konulara ilişkin olarak Devletin ekonomik ve mali çıkarlarının korunması için gerekli olması.</p>


            <span class="fw-bold text-dark mt-3">Veri Güvenliği için Alınan Tedbirler</span>
            <p class="mt-3">Şirket , kişisel verilerin korunması için gerekli olan uygun güvenlik düzeyini temin etmeye yönelik gerekli her türlü teknik ve idari tedbirleri almaktadır. KVKK’nın 12(1). maddesinde öngörülen tedbirler şunlardır:</p>

            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin hukuka aykırı olarak işlenmesini önlemek,</li>
            <li><i class="fas fa-angle-right"></i> Kişisel verilere hukuka aykırı olarak erişilmesini önlemek,</li> 
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin muhafazasını sağlamak.</li></ul>

            <p class="mt-3">Bu kapsamda alınan önlemler aşağıda sayılmıştır:</p>

            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Kendi kurum veya kuruluşunda, Kanun hükümlerinin uygulanmasını sağlamak amacıyla gerekli denetimleri yapar ve yaptırır</li>
            <li><i class="fas fa-angle-right"></i> İşlenen kişisel verilerin kanuni olmayan yollarla başkaları tarafından elde edilmesi hâlinde, durumu en kısa sürede ilgilisine ve Kurula bildirir.</li>
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin paylaşılması ile ilgili olarak, kişisel verilerin paylaşıldığı kişiler ile çerçeve sözleşme imzalar yahut sözleşmelere ekleyeceği hükümler ile veri güvenliğini sağlar.</li>
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin işlenmesi hakkında bilgili ve deneyimli personel istihdam eder ve personeline gerekli KVK eğitimlerini verir.</li>
            <li><i class="fas fa-angle-right"></i> Veri güvenliğini sağlamak amacıyla bilgili ve deneyimli kişiler istihdam eder ve personeline gerekli KVK eğitimlerini verir.</li> 
            <li><i class="fas fa-angle-right"></i> Kurulan sistemler kapsamında gerekli iç kontrolleri yapar.</li>
            <li><i class="fas fa-angle-right"></i> Kurulan sistemler kapsamında risk analizi, veri sınıflandırması, BT risk değerlendirmesi ve iş etki analizinin gerçekleştirilmesi süreçlerini yürütür.</li>
            <li><i class="fas fa-angle-right"></i> Kişisel verilerin kurum dışına sızmasını engelleyecek ve/veya gözlemleyecek teknik altyapının temin edilmesini ve ilgili matrislerin oluşturulmasını sağlar.</li>
            <li><i class="fas fa-angle-right"></i> Bilgi teknolojileri birimlerinde çalışanların kişisel verilere erişimi yetkilerinin kontrol altında tutulmasını sağlar.</li></ul>

            <span class="fw-bold text-dark mt-3">Görüntü Kayıtlarının İşlenmesi</span>
            <p class="mt-3">Şirket tarafından, Şirket tesis ve işletmelerinin genel ve ticari güvenliğinin temin edebilmesi amacıyla, KVKK’da öngörülen ve işbu Politika’da yer verilen temel ilkelere uygun olarak ziyaretçilerin, çalışanların ve diğer ilgili kişilerin görüntü kaydı alınmakta ve bu kayıtlar işlenme amaçlarına uygun süre zarfında fiziksel veya elektronik ortamda güvenli bir şekilde saklanmaktadır. Görüntü kaydı alınan yerlerde, veri sahiplerinin bilgilendirilmesi amacıyla, görüntü kaydı alındığına dair uyarı görünür bir biçimde yer almaktadır. Söz konusu faaliyetler kapsamında ŞİRKET tarafından kişisel verilerin korunmasına ilişkin öngörülen yükümlülüklere uygun hareket edilmektedir. Mahremiyetin yüksek olduğu yerlerde ise görüntüleme yapılmamaktadır.</p>

            <span class="fw-bold text-dark">Amaç</span>
            <p class="mt-3">Mehtap Erciyes Diyetisyen MÜH. GIDA TAAH. PETROL ÜR. SAN. VE TİC. LTD. ŞTİ. (Mehtap Erciyes Diyetisyen “Şirket”) olarak, 6698 Sayılı Kişisel Verilerin Korunması Kanunu’nun (“KVKK” veya ‘‘Kanun’’) yürürlüğe girdiği tarihten itibaren, ticari faaliyetlerimizi yerine getirirken herhangi bir şekilde temas ettiğimiz tüm gerçek kişilere ait kişisel verilerin korunmasına ve bu çerçevede KVKK’da yer alan gerekliliklerin eksiksiz olarak yerine getirilmesine büyük önem vermekteyiz. İşbu Kişisel Verilerin Korunması ve Gizlilik Politikası (“Politika”), kişisel verilerin şirket tarafından toplanması, kullanılması, paylaşması ve saklanması süreçleri ve prensipleri hakkında sizleri bilgilendirmek amacıyla hazırlanmıştır. İşbu Politika’da, şirket tarafından veri sahiplerine ait kişisel verilerin işlenmesine ilişkin esaslara KVKK’da yer alan düzenleme sırasına uygun olarak yer verilmiş olup, bu açıklamalar şirket çalışanlarını, aktif ve potansiyel müşterilerimizi, ziyaretçilerimizi ve şirket ile ilişki içinde bulunan diğer gerçek kişileri kapsamaktadır.</p>
  
            <p><span class="fw-bold text-dark">Kapsam:</span> Bu Politika, Mehtap Erciyes Diyetisyenın tüm iş ve işlem süreçlerinde uygulanır.</p> 
            <p class="text-start">1. Veri Sorumlusu ve Temsilcisi KVKK uyarınca, MEhtap Erciyes Diyetisyen olarak, veri sorumlusu sıfatıyla, kişisel verilerinizi aşağıda açıklanan amaçlar kapsamında; hukuka ve dürüstlük kurallarına uygun bir şekilde işleyebilecek, kaydedebilecek, saklayabilecek, sınıflandırabilecek, güncelleyebilecek ve mevzuatın izin verdiği hallerde ve/veya işlendikleri amaçla sınırlı olarak 3. kişilere açıklayabilecek/aktarabileceğiz.</p>

            <span class="fw-bold text-dark">Kaynaklar</span>
            <p class="mt-3">6698 Sayılı Kişisel Verilerin Korunması Kanunu,</p>
            <p class="mt-3">Kişisel Verilerin Silinmesi, Yok Edilmesi veya Anonim Hale Getirilmesi Hakkında Yönetmelik.</p>
  
            <span class="fw-bold text-dark">Tanımlamalar</span>
            <p class="mt-3"><span class="fw-bold text-dark">Açık Rıza:</span> Belirli bir konuya ilişkin, bilgilendirilmeye dayanan ve özgür iradeyle açıklanan rıza.</p> 
            <p><span class="fw-bold text-dark">İlgili Kullanıcı:</span> Verilerin teknik olarak depolanması, korunması ve yedeklenmesinden sorumlu olan kişi ya da birim hariç olmak üzere veri sorumlusu organizasyonu içerisinde veya veri sorumlusundan aldığı yetki ve talimat doğrultusunda kişisel verileri işleyen kişilerdir.</p> 
            <p><span class="fw-bold text-dark">İmha:</span> Kişisel verilerin silinmesi, yok edilmesi veya anonim hale getirilmesi.</p> 
            <p><span class="fw-bold text-dark">Kanun/KVKK:</span> 6698 Sayılı Kişisel Verilerin Korunması Kanunu.</p>
            <p><span class="fw-bold text-dark">Kayıt Ortamı:</span> Tamamen veya kısmen otomatik olan ya da herhangi bir veri kayıt sisteminin parçası olmak kaydıyla otomatik olmayan yollarla işlenen kişisel verilerin bulunduğu her türlü ortam</p>
            <p><span class="fw-bold text-dark">Kişisel Veri:</span> Kimliği belirli veya belirlenebilir gerçek kişiye ilişkin her türlü bilgi. Kişisel Verilerin İşlenmesi : Kişisel verilerin tamamen veya kısmen otomatik olan ya da herhangi bir veri kayıt sisteminin parçası olmak kaydıyla otomatik olmayan yollarla elde edilmesi, kaydedilmesi, depolanması, muhafaza edilmesi, değiştirilmesi, yeniden düzenlenmesi, açıklanması, aktarılması, devralınması, elde edilebilir hâle getirilmesi, sınıflandırılması ya da kullanılmasının engellenmesi gibi veriler üzerinde gerçekleştirilen her türlü işlem.</p>
            <p><span class="fw-bold text-dark">Kişisel Verilerin Anonim Hale Getirilmesi:</span> Kişisel verilerin, başka verilerle eşleştirilerek dahi hiçbir surette kimliği belirli veya belirlenebilir bir gerçek kişiyle ilişkilendirilemeyecek hâle getirilmesi.</p>
            <p><span class="fw-bold text-dark">Kişisel Verilerin Silinmesi:</span> Kişisel verilerin silinmesi; kişisel verilerin İlgili Kullanıcılar için hiçbir şekilde erişilemez ve tekrar kullanılamaz hale getirilmesi.</p>
            <p><span class="fw-bold text-dark">Kişisel Verilerin Yok Edilmesi:</span> Kişisel verilerin hiç kimse tarafından hiçbir şekilde erişilemez, geri getirilemez ve tekrar kullanılamaz hale getirilmesi işlemi.</p>
            <p><span class="fw-bold text-dark">Kurul:</span> Kişisel Verileri Koruma Kurulu.</p>
            <p><span class="fw-bold text-dark">Özel Nitelikli Kişisel Veri:</span> Kişilerin ırkı, etnik kökeni, siyasi düşüncesi, felsefi inancı, dini, mezhebi veya diğer inançları, kılık ve kıyafeti, dernek, vakıf ya da sendika üyeliği, sağlığı, cinsel hayatı, ceza mahkûmiyeti ve güvenlik tedbirleriyle ilgili verileri ile biyometrik ve genetik verileri.</p>
            <p><span class="fw-bold text-dark">Periyodik İmha:</span> Kanun’da yer alan kişisel verilerin işlenme şartlarının tamamının ortadan kalkması durumunda kişisel verileri saklama ve imha politikasında belirtilen ve tekrar eden aralıklarla re’sen gerçekleştirilecek silme, yok etme veya anonim hale getirme işlemi.</p>
            <p><span class="fw-bold text-dark">Veri Sahibi/İlgili Kişi:</span> Kişisel verisi işlenen gerçek kişi.</p>
            <p><span class="fw-bold text-dark">Veri İşleyen:</span> Veri sorumlusunun verdiği yetkiye dayanarak onun adına kişisel verileri işleyen gerçek veya tüzel kişi.</p>
            <p><span class="fw-bold text-dark">Veri Sorumlusu:</span> Kişisel verilerin işleme amaçlarını ve vasıtalarını belirleyen, veri kayıt sisteminin kurulmasından ve yönetilmesinden sorumlu olan gerçek veya tüzel kişi.</p>
            <p><span class="fw-bold text-dark">Yönetmelik:</span> 28 Ekim 2017 tarihinde Resmi Gazete’de yayımlanan Kişisel Verilerin Silinmesi, Yok Edilmesi veya Anonim Hale Getirilmesi Hakkında Yönetmelik.</p>

            <span class="fw-bold text-dark">Kişisel Verilerin Korunmasına İlişkin Usul ve Esaslar</span>
            <span class="fw-bold text-dark mt-3">GENEL İLKELER</span>
            <p class="mt-3">Mehtap Erciyes Diyetisyen, kişisel verileri KVKK ve ilgili diğer kanunlarda öngörülen usul ve esaslara uygun olarak işlemektedir. Bu çerçevede, şirket tarafından kişisel veriler işlenirken KVKK’da yer alan ilkelere uygunluk sağlanmaktadır.</p>
            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Hukuka ve dürüstlük kurallarına uygun olma: Bu ilke uyarınca, Mehtap Erciyes Diyetisyen’nun veri işleme süreçleri başta Anayasa ve KVKK olmak üzere ilgili tüm mevzuat ile dürüstlük kurallarının gerektirdiği sınırlar içinde kalınarak yürütülmektedir.</li>
            <li><i class="fas fa-angle-right"></i> Doğru ve gerektiğinde güncel olma: Mehtap Erciyes Diyetisyen tarafından işlenen kişisel verilerin doğru ve güncel duruma uygun olması için gerekli tedbirler alınmakta ve işlenmekte olan verilerin gerçek durumu yansıtmasını sağlamak amacıyla bilgilendirmeler de bulunularak veri sahiplerine gerekli imkânlar tanınmaktadır.</li>
            <li><i class="fas fa-angle-right"></i> Belirli, açık ve meşru amaçlar için işlenme: şirket yalnızca açık ve kesin olarak belirlenen meşru amaçlarla kişisel veri işlemekte olup, bu amaçlar dışında veri işleme faaliyetinde bulunmamaktadır. Bu kapsamda, yalnızca veri sahipleriyle kurulan iş ilişkisi ile bağlantılı ve sınırlı olarak ve bunlar açısından gerekli olması halinde kişisel veri işlemektedir.</li>

            <li><i class="fas fa-angle-right"></i> İşlendikleri amaçla bağlantılı, sınırlı ve ölçülü olma: veriler, KVKK ve ilgili diğer mevzuata uygun olarak, veri kategorilerine göre belirlenen amaçların gerçekleştirilebilmesine elverişli, amacın gerçekleştirilmesiyle ilgili ve ölçülü olarak işlenmekte olup, ihtiyaç duyulmayan kişisel verilerin işlenmesinden kaçınılmaktadır.</li>
            <li><i class="fas fa-angle-right"></i> İlgili mevzuatta öngörülen veya işlendikleri amaç için gerekli olan süre kadar muhafaza edilme: işlenen kişisel veriler, ancak ilgili mevzuatta öngörülen veya işlendikleri amaç için gerekli olan süre kadar muhafaza edilmektedir. Bu kapsamda, şirket , ilgili mevzuatta verilerin saklanması için öngörülen bir süre varsa bu süreye uymakta; böyle bir süre yoksa verileri, ancak işlendikleri amaç için gerekli olan süre kadar muhafaza etmektedir.</li> 
            </ul>

            <span class="fw-bold text-dark mt-3">Kişisel Verilerin İşlenme Şartları</span>
            <p class="mt-3">KVKK ile kişisel verilerin işlenme koşulları düzenlenmiş olup, Mehtap Erciyes Diyetisyen tarafından kişisel veriler aşağıda belirtilen söz konusu koşullara uygun olarak işlenmektedir.</p>
            <p class="mt-3">Kanun'da sayılan istisnalar dışında, şirket ancak veri sahiplerinin açık rızasını temin etmek suretiyle kişisel veri işlemektedir. Kanun’da sayılan aşağıdaki hallerin varlığı durumunda ise, veri sahibinin açık rızası olmasa dahi kişisel veriler işlenebilmektedir.</p>

            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Kanunlarda açıkça öngörülmesi,</li>
            <li><i class="fas fa-angle-right"></i> Fiili imkânsızlık nedeniyle rızasını açıklayamayacak durumda bulunan veya rızasına hukuki geçerlilik tanınmayan kişinin kendisinin ya da bir başkasının hayatı veya beden bütünlüğünün korunması için zorunlu olması,</li>
            <li><i class="fas fa-angle-right"></i> Bir sözleşmenin kurulması veya ifasıyla doğrudan doğruya ilgili olması kaydıyla sözleşmenin taraflarına ait kişisel verilerin işlenmesinin gerekli olması.</li>
            <li><i class="fas fa-angle-right"></i> Veri sorumlusunun hukuki yükümlülüğünü yerine getirebilmesi için zorunlu olması,</li>
            <li><i class="fas fa-angle-right"></i> Veri sahibinin kendisi tarafından alenileştirilmiş olması,</li>
            <li><i class="fas fa-angle-right"></i> Bir hakkın tesisi, kullanılması veya korunması için veri işlemenin zorunlu olması,</li> 
            <li><i class="fas fa-angle-right"></i> Veri sahibinin temel hak ve özgürlüklerine zarar vermemek kaydıyla, veri sorumlusunun meşru menfaatleri için veri işlenmesinin zorunlu olması.</li></ul>

            <p class="mt-3">Özel nitelikli kişisel verilerin işlenmesinde ise şirket tarafından özel hassasiyet gösterilmektedir. Kurul tarafından belirlenen önlemlerin alınması şartıyla bu tür veriler, veri sahiplerinin açık rızası olmaksızın işlenmemektedir. Sağlık ve cinsel hayata ilişkin veriler ise yeterli önlemlerin alınması şartıyla ve şu sebeplerin varlığı halinde açık rızası alınmaksızın işlenebilmektedir:</p>

            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> Kamu sağlığının korunması, Koruyucu hekimlik, Tıbbî teşhis, Tedavi ve bakım hizmetlerinin yürütülmesi, Sağlık hizmetleri ile finansmanının planlanması ve yönetimi.</li></ul>

            <span class="fw-bold text-dark mt-3">Kişisel Verilerin İşlenme Amaçları</span>
            <p class="mt-3">Şirket tarafından elde edilen kişisel verileriniz, aşağıda açıklanan kapsamlar dahilinde işlenebilecektir:</p>
            <ul class="ulreset">
            <li><i class="fas fa-angle-right"></i> İK operasyonları,</li>
            <li><i class="fas fa-angle-right"></i> Şirket içi operasyonları,</li> 
